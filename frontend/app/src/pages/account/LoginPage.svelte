<script lang="typescript">
  import { postApi } from "@app/services/ApiService";
  import { setToken } from "@app/services/AuthService";
  import { afterUpdate, onMount } from "svelte";
  import type { UserToken } from "@app/services/AuthService";
  import Button from "@app/components/Button.svelte";
  import LogoFull from "@app/components/LogoFull.svelte";

  let email = "";
  let password = "";
  let remember = true;
  let valid = false;
  let showPassword = false;
  let processing = false;
  let error = "";
  let processingGoogle = false;
  let googleIdToken = "";

  onMount(() => {
    //@ts-ignore
    window.onGoogleSignIn = (user: any) => {
      processingGoogle = true;
      const idToken = user.getAuthResponse().id_token;
      googleIdToken = idToken;
      onGoogleLogin();
    };
  });

  async function onGoogleLogin() {
    remember = true;
    await doLogin("user/login/google", {
      token: googleIdToken,
    });
  }

  async function onLogin() {
    error = "";
    processing = true;
    email = email.trim().toLowerCase();
    await doLogin("user/login", {
      email,
      password,
    });
  }

  async function doLogin(endpoint: string, body: any) {
    try {
      const result = await postApi<UserToken>(endpoint, body);
      if (result.token) {
        setToken(result, remember);
        const redirect = sessionStorage.getItem("redirect_after_login");
        if (redirect) {
          window.location.replace(redirect);
        } else {
          window.location.replace("/dashboard");
        }
      } else {
        error = "Failed to login, unknown response from server.";
      }
    } catch (ex) {
      if (ex.message === "invalid username or password") {
        error = "Invalid username or password.";
      } else {
        console.error(ex);
        error = "Failed to login, something went wrong.";
      }
    } finally {
      processing = false;
    }
  }

  afterUpdate(() => {
    valid = isValid();
  });

  function isValid(): boolean {
    return email != "" && password != "";
  }

  function togglePassword() {
    showPassword = !showPassword;
  }
</script>

<style>
  :global(input:required) {
    box-shadow: none;
  }
</style>

<svelte:head>
  <meta
    name="google-signin-client_id"
    content="807768276065-c8b2jvlks20jgm0mk3t2akfm7pc3jomu.apps.googleusercontent.com" />
  <script src="https://apis.google.com/js/platform.js" async defer>
  </script>
</svelte:head>

<div
  class="min-h-screen bg-gray-100 flex flex-col justify-center py-12 sm:px-6
    lg:px-8">
  <div class="sm:mx-auto sm:w-full sm:max-w-md">
    <h2
      class="mt-6 text-center text-3xl leading-9 font-extrabold text-gray-900">
      Sign in to our platform
    </h2>
    <p class="mt-2 text-center text-sm leading-5 text-gray-600 max-w">
      Or <a href="/account/register" class="font-medium text-indigo-600
          hover:text-indigo-500 focus:outline-none focus:underline transition
          ease-in-out duration-150"> Sign up </a>
    </p>
  </div>

  <div class="sm:mx-auto sm:w-full sm:max-w-md">
    {#if error}
      <div class="inset-x-0 pt-2 mb-3">
        <div class="max-w-screen-xl mx-auto px-2 sm:px-6 lg:px-8">
          <div class="p-2 rounded-md bg-red-600 shadow-lg sm:p-2">
            <div class="flex items-center justify-between flex-wrap">
              <div class="w-0 flex-1 flex items-center">
                <p class="ml-3 font-medium text-white truncate">
                  <span> {error} </span>
                </p>
              </div>
            </div>
          </div>
        </div>
      </div>
    {/if}

    <div class="bg-white py-8 px-4 shadow sm:rounded-lg sm:px-10 mt-3">
      <div style="margin-bottom: 1em">
        <div
          class="g-signin2"
          data-longtitle="true"
          data-theme="dark"
          data-height="50"
          data-width="365"
          data-onsuccess="onGoogleSignIn" />
      </div>

      <div class="mt-6">
        <div class="relative">
          <div class="absolute inset-0 flex items-center">
            <div class="w-full border-t border-gray-300" />
          </div>
          <div class="relative flex justify-center text-sm leading-5">
            <span class="px-2 bg-white text-gray-500"> Or </span>
          </div>
        </div>

        <form on:submit|preventDefault|stopPropagation={onLogin}>
          <label
            for="email"
            class="block text-sm font-medium leading-5 text-gray-700">Email</label>
          <div class="mt-1 relative rounded-md shadow-sm">
            <div
              class="absolute inset-y-0 left-0 pl-3 flex items-center
                pointer-events-none">
              <svg
                class="w-5 h-5"
                xmlns="http://www.w3.org/2000/svg"
                viewBox="0 0 20 20"
                fill="currentColor">
                <path
                  d="M2.003 5.884L10 9.882l7.997-3.998A2 2 0 0016 4H4a2 2 0 00-1.997 1.884z" />
                <path
                  d="M18 8.118l-8 4-8-4V14a2 2 0 002 2h12a2 2 0 002-2V8.118z" />
              </svg>
            </div>
            <input
              id="email"
              autocomplete="email"
              type="email"
              bind:value={email}
              class="form-input block w-full pl-10 sm:text-sm sm:leading-5" />
          </div>
          <div class="mt-2">
            <label
              for="password"
              class="block text-sm font-medium leading-5 text-gray-700">Password</label>
            <div class="mt-1 relative rounded-md shadow-sm">
              <div
                class="absolute inset-y-0 left-0 pl-3 flex items-center
                  pointer-events-none">
                <svg
                  class="h-5 w-5"
                  xmlns="http://www.w3.org/2000/svg"
                  viewBox="0 0 20 20"
                  fill="currentColor">
                  <path
                    fill-rule="evenodd"
                    d="M5 9V7a5 5 0 0110 0v2a2 2 0 012 2v5a2 2 0 01-2 2H5a2 2 0 01-2-2v-5a2 2 0 012-2zm8-2v2H7V7a3 3 0 016 0z"
                    clip-rule="evenodd" />
                </svg>
              </div>
              {#if showPassword}
                <input
                  id="password"
                  type="text"
                  minlength="5"
                  required
                  autocomplete="current-password"
                  bind:value={password}
                  class="form-input block w-full pl-10 sm:text-sm sm:leading-5" />
                <div
                  class="absolute inset-y-0 right-0 pr-3 flex items-center
                    cursor-pointer"
                  on:click={togglePassword}>
                  <svg
                    class="w-5 h-5"
                    xmlns="http://www.w3.org/2000/svg"
                    fill="none"
                    viewBox="0 0 24 24"
                    stroke="currentColor">
                    <path
                      stroke-linecap="round"
                      stroke-linejoin="round"
                      stroke-width="2"
                      d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21" />
                  </svg>
                </div>
              {:else}
                <input
                  id="password"
                  type="password"
                  minlength="5"
                  required
                  autocomplete="current-password"
                  bind:value={password}
                  class="form-input block w-full pl-10 sm:text-sm sm:leading-5" />
                <div
                  class="absolute inset-y-0 right-0 pr-3 flex items-center
                    cursor-pointer"
                  on:click={togglePassword}>
                  <svg
                    class="w-5 h-5"
                    xmlns="http://www.w3.org/2000/svg"
                    fill="none"
                    viewBox="0 0 24 24"
                    stroke="currentColor">
                    <path
                      stroke-linecap="round"
                      stroke-linejoin="round"
                      stroke-width="2"
                      d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
                    <path
                      stroke-linecap="round"
                      stroke-linejoin="round"
                      stroke-width="2"
                      d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z" />
                  </svg>
                </div>
              {/if}
            </div>
          </div>
          <div class="mt-6 flex items-center justify-between">
            <div class="flex items-center">
              <input
                id="remember_me"
                bind:checked={remember}
                type="checkbox"
                class="form-checkbox h-4 w-4 text-indigo-600 transition
                  duration-150 ease-in-out" />
              <label
                for="remember_me"
                class="ml-2 block text-sm leading-5 text-gray-900">
                Remember me
              </label>
            </div>

            <div class="text-sm leading-5">
              <a
                href="/account/forgot"
                class="font-medium text-indigo-600 hover:text-indigo-500
                  focus:outline-none focus:underline transition ease-in-out
                  duration-150">
                Forgot your password?
              </a>
            </div>
          </div>

          <div class="mt-5">
            {#if !processing}
              <Button type="primary" width={'full'} submit={true}>
                Sign In With Email
              </Button>
            {:else}
              <Button type="primary" width={'full'} disabled={true}>
                Signing In...
              </Button>
            {/if}
          </div>
        </form>
      </div>
    </div>
  </div>
</div>
